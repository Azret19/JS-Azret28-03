const button1 = document.querySelector('#Button_1')
const answer1 = document.querySelector('#Answer1')
button1.onclick =()=> {
    button1.innerHTML === 'Показать ответ' ? button1.innerHTML = 'Скрыть ответ': button1.innerHTML = 'Показать ответ'
    answer1.innerHTML === 'Ответ: Часы' ? answer1.innerHTML = '': answer1.innerHTML = 'Ответ: Часы'
    button1.classList.toggle('Button-1')
    answer1.classList.toggle('Answer-1')
}

